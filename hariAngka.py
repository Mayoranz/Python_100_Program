hari = int(input("Masukkan nomor hari (1-7): "))
if hari == 1:
    print("Senin")
elif hari == 2:
    print("Selasa")
elif hari == 3:
    print("Rabu")
elif hari == 4:
    print("Kamis")
elif hari == 5:
    print("Jumat")
elif hari == 6:
    print("Sabtu")
elif hari == 7:
    print("Minggu")
else:
    print("Nomor hari tidak valid.")